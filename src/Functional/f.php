    public static function call(string $name, array $arguments) : mixed
    {
        if (function_exists($name) === false) {
            require_once __DIR__ . DIRECTORY_SEPARATOR . str_replace('_', '', ucwords($name, '_')) . '.php';
        }
        return $name(...$arguments);
    }
